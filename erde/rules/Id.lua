-- -----------------------------------------------------------------------------
-- Id
-- -----------------------------------------------------------------------------

local Id = { ruleName = 'Id' }

-- -----------------------------------------------------------------------------
-- Parse
-- -----------------------------------------------------------------------------

function Id.parse(ctx)
  local node = ctx:OptChain()
  local last = node[#node]

  if last and last.variant == 'functionCall' then
    error()
  end

  return node
end

-- -----------------------------------------------------------------------------
-- Return
-- -----------------------------------------------------------------------------

return Id
