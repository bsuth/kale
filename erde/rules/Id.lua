-- -----------------------------------------------------------------------------
-- Id
-- -----------------------------------------------------------------------------

local Id = { ruleName = 'Id' }

-- -----------------------------------------------------------------------------
-- Parse
-- -----------------------------------------------------------------------------

function Id.parse(ctx)
  local node = ctx:OptChain()
  local last = node[#node]

  if last then
    if last.variant == 'functionCall' then
      error('Unexpected function call')
    end
  end

  return node
end

-- -----------------------------------------------------------------------------
-- Return
-- -----------------------------------------------------------------------------

return Id
