-- -----------------------------------------------------------------------------
-- Break
-- -----------------------------------------------------------------------------

local Break = { ruleName = 'Break' }

-- -----------------------------------------------------------------------------
-- Parse
-- -----------------------------------------------------------------------------

function Break.parse(ctx)
  return ctx:branchWord('break') and {} or ctx:throwExpected('break')
end

-- -----------------------------------------------------------------------------
-- Compile
-- -----------------------------------------------------------------------------

function Break.compile(ctx, node)
  return 'break'
end

-- -----------------------------------------------------------------------------
-- Return
-- -----------------------------------------------------------------------------

return Break
