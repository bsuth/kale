-- -----------------------------------------------------------------------------
-- Break
-- -----------------------------------------------------------------------------

local Break = {}

-- -----------------------------------------------------------------------------
-- Parse
-- -----------------------------------------------------------------------------

function Break.parse(ctx)
  return ctx:branchWord('break') and { rule = 'Break' }
    or ctx:throwExpected('break')
end

-- -----------------------------------------------------------------------------
-- Compile
-- -----------------------------------------------------------------------------

function Break.compile(ctx, node)
  return 'break'
end

-- -----------------------------------------------------------------------------
-- Return
-- -----------------------------------------------------------------------------

return Break
