-- -----------------------------------------------------------------------------
-- Break
-- -----------------------------------------------------------------------------

local Break = { ruleName = 'Break' }

-- -----------------------------------------------------------------------------
-- Parse
-- -----------------------------------------------------------------------------

function Break.parse(ctx)
  assert(ctx.loopBlock ~= nil)
  ctx:consume()
  return {}
end

-- -----------------------------------------------------------------------------
-- Compile
-- -----------------------------------------------------------------------------

function Break.compile(ctx, node)
  return 'break'
end

-- -----------------------------------------------------------------------------
-- Return
-- -----------------------------------------------------------------------------

return Break
