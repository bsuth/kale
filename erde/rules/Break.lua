-- -----------------------------------------------------------------------------
-- Break
-- -----------------------------------------------------------------------------

local Break = { ruleName = 'Break' }

-- -----------------------------------------------------------------------------
-- Parse
-- -----------------------------------------------------------------------------

function Break.parse(ctx)
  local node = {}

  if ctx.loopBlock == nil then
    -- No loop for `break`
    error()
  elseif not ctx:branchWord('break') then
    error()
  end

  return node
end

-- -----------------------------------------------------------------------------
-- Compile
-- -----------------------------------------------------------------------------

function Break.compile(ctx, node)
  return 'break'
end

-- -----------------------------------------------------------------------------
-- Return
-- -----------------------------------------------------------------------------

return Break
